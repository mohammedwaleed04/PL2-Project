    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        String id = QuestionIdText.getText();
        String name  = QuestionText.getText();
        String opt1 = Opt1Text.getText();
        String opt2 = Opt2Text.getText();
        String opt3 = Opt3Text.getText();
        String opt4 = Opt4Text.getText();
        String answer = AnswerText.getText();
        String CourseName =  CNameText.getText();
        try
        {
           Connection con = ConnectionProvider.getCon();
           PreparedStatement pa = con.prepareStatement("update question set name=?,opt1=?,opt2=?,opt3=?,opt4=?,answer=?,CourseName=? where id=? ");           
           pa.setString(1, name);
           pa.setString(2, opt1);
           pa.setString(3, opt2 );
           pa.setString(4, opt3 );
           pa.setString(5, opt4 );
           pa.setString(6, answer); 
           pa.setString(7, CourseName);        
           pa.setString(8, id);

           pa.executeUpdate();
           JFrame jf = new JFrame();
           jf.setAlwaysOnTop(true);
           JOptionPane.showMessageDialog(jf , "successfully Updated");
           setVisible(false);
           new updateQuestion().setVisible(true);                    
        }
        catch (Exception e)
        {
            JFrame jf = new JFrame();           
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf , e);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

